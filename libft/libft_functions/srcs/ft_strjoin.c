#include "libft.h"

static void	ft_strcpy(char *dest, const char *src, size_t start, size_t len)
{
	size_t	i;

	i = 0;
	while (i < len)
	{
		dest[start + i] = src[i];
		i++;
	}
}

char	*ft_strjoin(const char *s1, const char *s2)
{
	size_t	len1;
	size_t	len2;
	char	*result;

	if (!s1 || !s2)
		return (NULL);
	len1 = ft_strlen(s1);
	len2 = ft_strlen(s2);
	result = ft_calloc(len1 + len2 + 1, sizeof(char));
	if (!result)
		return (NULL);
	ft_strcpy(result, s1, 0, len1);
	ft_strcpy(result, s2, len1, len2);
	return (result);
}
