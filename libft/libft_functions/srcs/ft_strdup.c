#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dest;
	size_t	src_len;
	size_t	i;

	src_len = 0;
	while (s[src_len])
		src_len++;
	dest = malloc(sizeof(char) * (src_len + 1));
	if (!dest)
		return (NULL);
	i = 0;
	while (s[i])
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
